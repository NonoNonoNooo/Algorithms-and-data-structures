def merge_sorted_arrays(arr1, arr2):
    """
    Об'єднує два упорядкованих масиви в один упорядкований.

    Параметри:
    - arr1 (list): Перший упорядкований масив.
    - arr2 (list): Другий упорядкований масив.

    Повертає:
    - list: Упорядкований масив, що містить всі елементи arr1 та arr2.

    """
    merged_arr = []  # Результуючий об'єднаний масив
    i = 0  # Індекс для першого масиву arr1
    j = 0  # Індекс для другого масиву arr2

    # Поки не досягнуто кінця хоча б одного з масивів
    while i < len(arr1) and j < len(arr2):
        # Порівнюємо елементи на поточних позиціях у двох масивах
        if arr1[i] < arr2[j]:
            # Якщо елемент з першого масиву менший, додаємо його до об'єднаного масиву
            merged_arr.append(arr1[i])
            i += 1  # Переходимо до наступного елементу у першому масиві
        else:
            # Якщо елемент з другого масиву менший, додаємо його до об'єднаного масиву
            merged_arr.append(arr2[j])
            j += 1  # Переходимо до наступного елементу у другому масиві

    # Додаємо залишкові елементи з першого масиву, якщо вони є
    while i < len(arr1):
        merged_arr.append(arr1[i])
        i += 1

    # Додаємо залишкові елементи з другого масиву, якщо вони є
    while j < len(arr2):
        merged_arr.append(arr2[j])
        j += 1

    return merged_arr

# Приклад використання
arr1 = [1, 3, 5, 7, 9]
arr2 = [2, 4, 6, 8, 10]
merged_arr = merge_sorted_arrays(arr1, arr2)
print("Об'єднаний упорядкований масив:")
print(merged_arr)
